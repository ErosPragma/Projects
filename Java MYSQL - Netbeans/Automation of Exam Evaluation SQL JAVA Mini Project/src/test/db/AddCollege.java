        // TODO add your handling code here:
        
        
        name = jTextField15.getText();
        address = jTextField17.getText();
            
        if ("".equals(name) || "".equals(address))
        {
            dispose();
            new AddCollege("Invalid Entry").show();
        }
        else
        {    
            
            id = Integer.parseInt(jTextField14.getText());
            pincode = Integer.parseInt(jTextField18.getText());
            if ( (int)(pincode/100000) != 6)
            {
                dispose();
                new AddCollege("Invalid Pincode").show();
            }
            
            try{  
                Class.forName(ms.driver);  
                     try (Connection con = DriverManager.getConnection(  
                    ms.url,ms.username,ms.password)) {
                Statement stmt=con.createStatement();
                stmt.executeUpdate("insert into college values ("+id+",\'"+name+"\',\'"+address+"\',"+pincode+");");
                dispose();
                new AddCollege("College Added Succesfully").show();
                }
            }
            catch(Exception e)
            { 
                System.out.println(e);
                dispose();
                new AddCollege("Invalid/Duplicate Entry").show();
            }  
        }
